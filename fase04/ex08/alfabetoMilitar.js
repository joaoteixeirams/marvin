function alfabetoMilitar(x) {
    if( x==="a") {return "alpha"}
    if( x==="A") {return "alpha"}
    if( x==="e") {return "echo"}
    if( x==="E") {return "echo"}
    if( x==="i") {return "india"}
    if( x==="I") {return "india"}
    if( x==="o") {return "oscar"}
    if( x==="O") {return "oscar"}
    if( x==="u") {return "uniform"}
    if( x==="U") {return "uniform"}
 else {return false}
}